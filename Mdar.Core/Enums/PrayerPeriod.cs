namespace Mdar.Core.Enums;

/// <summary>
/// الفترات الزمنية اليومية المبنية على أوقات الصلاة.
/// يُقسَّم اليوم إلى 6 فترات تغطي 24 ساعة بالكامل دون تداخل.
///
/// التوزيع الزمني التقريبي (يتغيّر حسب الموسم والموقع):
/// ┌───────────────┬──────────────────────────────┐
/// │ الفترة        │ النطاق الزمني التقريبي        │
/// ├───────────────┼──────────────────────────────┤
/// │ AfterFajr     │ الفجر → الشروق  (~4:30-6:00) │
/// │ Duha          │ الشروق → الظهر  (~6:00-12:00)│
/// │ AfterDhuhr    │ الظهر → العصر   (~12:00-15:30)│
/// │ AfterAsr      │ العصر → المغرب  (~15:30-18:00)│
/// │ AfterMaghrib  │ المغرب → العشاء (~18:00-19:30)│
/// │ AfterIsha     │ العشاء → الفجر  (~19:30-4:30) │
/// └───────────────┴──────────────────────────────┘
///
/// كل فترة لها شخصية إنتاجية مختلفة تؤثر في محرك الأولويات.
/// </summary>
public enum PrayerPeriod
{
    /// <summary>
    /// بعد الفجر حتى الشروق.
    /// أقصر فترات اليوم لكن أعمقها تركيزاً.
    /// مثالية لـ: التأمل، القراءة الروحية، تخطيط اليوم.
    /// درجة التركيز: ★★★ (عالية جداً)
    /// </summary>
    AfterFajr = 0,

    /// <summary>
    /// من الشروق حتى الظهر (وقت الضحى).
    /// ذروة الطاقة والتركيز في اليوم — الذاكرة والتحليل في أوجّهما.
    /// مثالية لـ: العمل العميق (Deep Work)، التعلم، الكتابة، البرمجة.
    /// درجة التركيز: ★★★ (عالية جداً)
    /// </summary>
    Duha = 1,

    /// <summary>
    /// من الظهر حتى العصر.
    /// انخفاض طفيف في التركيز بعد الغداء.
    /// مثالية لـ: الاجتماعات، الردود البريدية، المهام الإدارية.
    /// درجة التركيز: ★★☆ (متوسطة)
    /// </summary>
    AfterDhuhr = 2,

    /// <summary>
    /// من العصر حتى المغرب.
    /// موجة طاقة ثانية — جيدة للمهام التنفيذية.
    /// مثالية لـ: المراجعة، التنفيذ، المكالمات، التواصل.
    /// درجة التركيز: ★★☆ (متوسطة)
    /// </summary>
    AfterAsr = 3,

    /// <summary>
    /// من المغرب حتى العشاء.
    /// وقت انتقالي قصير — عائلي وشخصي.
    /// مثالية لـ: الأسرة، الاسترخاء، المهام الخفيفة.
    /// درجة التركيز: ★☆☆ (منخفضة)
    /// </summary>
    AfterMaghrib = 4,

    /// <summary>
    /// من العشاء حتى الفجر (الليل).
    /// هادئ لكن الطاقة في تراجع.
    /// مثالية لـ: التخطيط لليوم التالي، القراءة الخفيفة، المراجعة.
    /// درجة التركيز: ★☆☆ (منخفضة)
    /// </summary>
    AfterIsha = 5
}
