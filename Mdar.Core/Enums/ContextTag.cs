namespace Mdar.Core.Enums;

/// <summary>
/// السياق المكاني (الموقع الفيزيائي أو البيئي) المطلوب لتنفيذ المهمة.
/// يُستخدم في محرك الأولويات لاستبعاد المهام غير الملائمة للموقع الحالي.
///
/// مثال:
///   المستخدم الآن في "المكتب" → يرى فقط مهام Office + Anywhere.
///   المستخدم "خارج" → يرى فقط مهام Outside + Anywhere.
/// </summary>
public enum ContextTag
{
    /// <summary>
    /// يمكن تنفيذها في أي مكان — لا قيد مكاني.
    /// القيمة الافتراضية لأغلب المهام.
    /// </summary>
    Anywhere = 0,

    /// <summary>
    /// تحتاج إلى بيئة المنزل (الهدوء، الأدوات المنزلية، الخصوصية).
    /// مثال: "ترتيب الغرفة"، "طبخ"، "جلسة تركيز عميق".
    /// </summary>
    Home = 1,

    /// <summary>
    /// تحتاج إلى بيئة العمل/المكتب (الجهاز، الملفات، الزملاء).
    /// مثال: "اجتماع مع الفريق"، "طباعة مستند".
    /// </summary>
    Office = 2,

    /// <summary>
    /// تُنفَّذ خارج المنزل/المكتب (تنقل، مهمات، نزهة).
    /// مثال: "شراء مستلزمات"، "زيارة عميل"، "إنجاز معاملة حكومية".
    /// </summary>
    Outside = 3,

    /// <summary>
    /// تحتاج فقط إلى اتصال بالإنترنت — أي موقع متاح.
    /// مثال: "رد على الإيميلات"، "بحث على الويب"، "اجتماع أونلاين".
    /// </summary>
    Online = 4
}
